// type -> c++ type
const TYPE_MAP = { // map
  string: 'std::string',
  int: 'int',
  double: 'double',
  bool: 'bool',
  void: 'void',
  auto: 'auto',
  json: 'nlohmann::json',
  bytes: 'std::vector<uint8_t>',
  'vector<string>': 'std::vector<std::string>',
  'vector<int>': 'std::vector<int>',
  http_response: 'std::string',
  file: 'std::ifstream',
};

function cppType(t) { return TYPE_MAP[t] || 'std::string'; }
function cppLiteral(v) { // js-ish literal to c++
  if (v === null || v === undefined) return '""'; // null
  if (typeof v === 'number') return String(v); // number
  if (typeof v === 'boolean') return v ? 'true' : 'false'; // bool
  if (typeof v === 'string') {
    if (/^\d+$/.test(v)) return v; // int literal
    if (v === 'true' || v === 'false') return v; // bool
    // escape for c++
    return JSON.stringify(v);
  }
  return JSON.stringify(v);
}

export function emitCpp(ir) { // main
  console.log('emitCpp', ir.program.name, ir.program.kind); // trace
  const out = []; // lines
  // header banner
  out.push(`// generated by nlpc - do not edit`); // banner
  out.push(`#include <iostream>`); // iostream
  out.push(`#include <string>`); // string
  out.push(`#include <vector>`); // vector
  out.push(`#include <sstream>`); // sstream
  // optional headers
  const reqs = (ir.requirements || []).map(r => r.name.toLowerCase()); // req names
  if (reqs.some(r => /fmt|format|spdlog|nlohmann|httplib|cpr|cpp-httplib/.test(r))) {
    // include only when needed
  }
  if (reqs.some(r => /nlohmann|json/.test(r))) out.push(`#include <nlohmann/json.hpp>`); // json
  if (reqs.some(r => /httplib|cpr|cpp-httplib/.test(r))) out.push(`#include <httplib.h>`); // http
  if (reqs.some(r => /spdlog/.test(r))) out.push(`#include <spdlog/spdlog.h>`); // spdlog
  if (reqs.some(r => /fmt/.test(r))) out.push(`#include <fmt/core.h>`); // fmt
  out.push(''); // blank
  // declarations (functions)
  for (const d of ir.declarations || []) { // loop
    if (d.kind === 'function') { // function
      const params = (d.params || []).map(p => `${cppType(p.type)} ${p.name}`).join(', '); // params
      out.push(`${cppType(d.returns || 'void')} ${d.name}(${params}) {`); // sig
      if (d.returns && d.returns !== 'void') { // has return
        out.push(`    return ${d.body?.length ? cppLiteral(d.body[0]?.value || '') : '""'};`); // simple return
      }
      out.push(`}`); // close
      out.push(''); // blank
    } else if (d.kind === 'variable') { // var
      out.push(`${cppType(d.type || 'string')} ${d.name} = ${cppLiteral(d.value)};`); // decl
    }
  }
  // main
  out.push(`int main() {`); // main
  out.push(`    std::ios::sync_with_stdio(false);`); // fast
  out.push(`    std::cin.tie(nullptr);`); // tie
  out.push(''); // blank
  // behaviors
  const startBeh = (ir.behaviors || []).find(b => b.trigger === 'start'); // start
  const routeBehs = (ir.behaviors || []).filter(b => b.trigger === 'route'); // routes
  if (routeBehs.length && reqs.some(r => /httplib|cpp-httplib/.test(r))) { // http server
    out.push(`    httplib::Server srv;`); // server
    for (const r of routeBehs) { // routes
      const method = (r.method || 'GET').toLowerCase(); // method
      const path = r.path || '/'; // path
      out.push(`    svr.${method}("${path}", [](const httplib::Request& req, httplib::Response& res) {`); // handler
      for (const s of r.body || []) { // body
        const ln = emitStmt(s, '        '); // emit
        if (ln) out.push(ln); // push
      }
      out.push(`        res.set_content("ok", "text/plain");`); // default resp
      out.push(`    });`); // close
    }
    out.push(`    svr.listen("0.0.0.0", 8080);`); // listen
  } else if (startBeh) { // console
    for (const s of startBeh.body || []) { // body
      const ln = emitStmt(s, '    '); // emit
      if (ln) out.push(ln); // push
    }
  }
  out.push(`    return 0;`); // ret
  out.push(`}`); // close
  return out.join('\n'); // join
}

function emitStmt(s, indent) { // one stmt
  if (!s || !s.kind) return null; // no
  switch (s.kind) { // dispatch
    case 'print': { // print
      const text = s.text || ''; // text
      // always use std::cout to avoid fmt v12 ABI issues; fmt can still be linked if user adds it later
      return `${indent}std::cout << ${JSON.stringify(text)} << std::endl;`; // cout
    }
    case 'ask': { // ask
      const name = s.name || 'input'; // name
      return [
        `${indent}std::cout << ${JSON.stringify(s.text || '> ')} << std::flush;`,
        `${indent}std::string ${name};`,
        `${indent}std::getline(std::cin, ${name});`,
      ].join('\n'); // multi
    }
    case 'set': { // set
      return `${indent}auto ${s.name} = ${cppLiteral(s.value)};`; // set
    }
    case 'call': { // call
      const args = (s.args || []).map(a => cppLiteral(a)).join(', '); // args
      return `${indent}${s.target}(${args});`; // call
    }
    case 'return': { // return
      return `${indent}return ${cppLiteral(s.value)};`; // return
    }
    case 'http_get': { // get
      const path = s.path || ''; // path
      return [
        `${indent}auto cli = httplib::Client("http://localhost", 8080);`,
        `${indent}auto res = cli.Get("${path}");`,
        `${indent}if (res) std::cout << res->body << std::endl;`,
      ].join('\n'); // join
    }
    case 'http_serve': { // serve
      const port = s.port || '8080'; // port
      return `${indent}svr.listen("0.0.0.0", ${port});`; // listen
    }
    case 'http_route': { // route
      return `${indent}svr.get("${s.path || '/'}", [](const auto& req, auto& res) { res.set_content("ok", "text/plain"); });`; // route
    }
    case 'json_load': { // json
      return `${indent}auto j = nlohmann::json::parse(std::ifstream("${s.path || 'data.json'}"));`; // load
    }
    case 'json_save': { // json save
      return `${indent}std::ofstream("${s.path || 'data.json'}") << nlohmann::json(${cppLiteral(s.value)}).dump();`; // save
    }
    case 'file_read': { // read
      return `${indent}std::ifstream in("${s.path || 'in.txt'}"); std::stringstream ss; ss << in.rdbuf(); auto ${s.name || 'data'} = ss.str();`; // read
    }
    case 'file_write': { // write
      return `${indent}std::ofstream("${s.path || 'out.txt'}") << ${cppLiteral(s.value)};`; // write
    }
    case 'log': { // log
      const lvl = s.level || 'info'; // level
      return `${indent}spdlog::${lvl}(${cppLiteral(s.text || '')});`; // log
    }
    case 'sleep': { // sleep
      return `${indent}std::this_thread::sleep_for(std::chrono::milliseconds(${s.ms || 1000}));`; // sleep
    }
    case 'assert': { // assert
      return `${indent}assert(${s.cond || 'true'});`; // assert
    }
    case 'for': { // for
      return `${indent}for (auto& ${s.iter || 'x'} : ${s.source || '{}'}) { /* TODO */ }`; // for
    }
    case 'if': { // if
      return `${indent}if (${s.cond || 'true'}) { /* TODO */ }`; // if
    }
    case 'raw': { // raw
      return `${indent}${s.code || ''}`; // raw
    }
    default: { // unknown
      return `${indent}// unsupported stmt: ${s.kind}`; // comment
    }
  }
}

